void solve(){
    int n;
    cin >> n;
    string s;
    cin >> s;

    string t = "tamocompetindo";

    for (int i = 0; i + t.size() <= s.size(); i++) {
        int cnt = 0;
        for (int j = 0; j < t.size(); j++) {
            if (t[j] != s[i + j]) cnt++;
            if (cnt > n) break;
        }
        if (cnt <= n) {
            cout << "SIM\n";
            return;
        }
    }
    cout << "NAO" << nl;
}


