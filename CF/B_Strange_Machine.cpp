void solve(){
    int n,m;
    cin >> n >> m;
    string s;
    cin >> s;

    int cnt = count(all(s),'B');

    while(m--){
        int x;
        cin >> x;
        if(cnt == 0) cout << x << nl;
        else{
            int ans = 0;
            int i = 0;
            while(x){
                if(s[i] == 'A') x--;
                else x /= 2;
                i++, i%=n;
                ans++;
            }
            cout << ans << nl;
        }
    }
    
}

